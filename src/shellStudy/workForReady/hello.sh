#!/bin/bash
#
echo "hello world"
type awk

# ----------------------------------------
## 知识点
##### * 在shell脚本的第一行的`#!/bin/bash`，后加上`-v`参数，表示：要打印程序的源代码
##### * 使用`type`可以查看各个命令的类型，例如：`type awk`


## 注意事项
##### * 表达式和运算符之间要有空格，例如`2+2`是不对的，要写成`2 + 2`

